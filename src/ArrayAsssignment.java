


public class ArrayAsssignment {

	public static void main(String[] args) {
			int row = 9;
			for(int i=1;i<=row;i++) {
				for(int j=1;j<4;j++) {
					System.out.print(" ");
					
				}
				for(int j=1;j<i;j++) {
					if(j%2!=0) {
						
					
					System.out.print("* ");
					}
				}
				System.out.println();
			}
			
		    
		}
	
}
